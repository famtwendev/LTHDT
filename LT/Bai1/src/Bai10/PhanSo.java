package Bai10;

public class PhanSo 
{
    private int tuso;
    private int mauso;
    
    public PhanSo()
    {
        this.tuso = 0;
        this.mauso = 0;
    }
    
    public void setTuso(int tuso)
    {
        this.tuso = tuso;
    }
    
    public void setMauso(int mauso)
    {
        this.mauso = mauso;
    }
    
    public int getTuso()
    {
        return tuso;
    }
    
    public int getMauso()
    {
        return mauso;
    }
    
    
            
}
